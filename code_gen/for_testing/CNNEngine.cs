namespace code_gen
{

  /// <summary>
  /// The CNNEngine class provides a CLS compliant, MWArray interface to the MATLAB
  /// functions contained in the files:
  /// <newpara></newpara>
  /// D:\Dropbox\Forex Projects\CNN\code_gen.mlx
